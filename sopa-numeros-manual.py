def create_numbersearch(numbers, size=28):
    grid = [['' for _ in range(size)] for _ in range(size)]
    directions = [(0, 1), (1, 0), (1, 1), (-1, 1)]

    def can_place(number, x, y, dx, dy):
        for i in range(len(number)):
            nx, ny = x + dx*i, y + dy*i
            if not (0 <= nx < size and 0 <= ny < size):
                return False
            if grid[ny][nx] not in ('', number[i]):
                return False
        return True

    def place_number(number):
        attempts = 100
        while attempts > 0:
            dx, dy = random.choice(directions)
            x = random.randint(0, size - 1)
            y = random.randint(0, size - 1)
            if can_place(number, x, y, dx, dy):
                for i in range(len(number)):
                    grid[y + dy*i][x + dx*i] = number[i]
                return True
            attempts -= 1
        return False

    for number in numbers:
        place_number(number)

    for y in range(size):
        for x in range(size):
            if grid[y][x] == '':
                grid[y][x] = random.choice(string.digits)

    return grid

